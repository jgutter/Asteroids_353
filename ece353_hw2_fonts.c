/**********************************************************
* Function Name: lcd_print_stringXY
**********************************************************
* Summary: prints a string to the LCD screen at a specified
* XY location in specified foreground and background colors
* X will specify the number of characters across with X=0
* being the left most character position, and X=13 being
* the right most.  Y will specify the number of characters
* down the screen with Y=0 being the top of the screen and
* Y=19 being the bottom row of characters.  NOTE this is
* opposite of how columns and rows are specified for the
* IL9341.
* Returns:
*  Nothing
**********************************************************/
void lcd_print_stringXY(
    char *msg, 
    int8_t X,
		int8_t Y,
    uint16_t fg_color, 
    uint16_t bg_color
)
{
	int ind;	//holds the index of character currently being processed
	uint32_t disp; 				//character being processed
	
	int8_t Xpos_Orig;			//holds the starting column of every line	
	int8_t Xpos;					//holds the comlumn that should be printed into next
	int8_t Ypos;					//holds the row that should be printed into next
	
	int16_t X_coord;			//holds the starting pixel of column based on actual LCD layout
	int16_t Y_coord;			//holds the starting pixel of row based on actual LCD layout
	
	Xpos_Orig = 13 - X; 	//assigns starting column from right to left 
	Xpos = 13 - X;				//assigns column from right to left 
	Ypos = 19 - Y;				//assigns column from right to left 

	

	//Loops through code length - 1 times, ignoring the EOF character
	for(ind=0; ind <= strlen(msg) -1; ind++){

		
			
			disp = (msg[ind] - ascii_Offset) * fontSize;	//assigns the current character
																										//and calculates to match our
																										//ASCII lookup table
		
		//CASE 1: if both column and row is valid
				if(Xpos >= 0 && Ypos >=0 ){
					X_coord = (Xpos*FONT_WIDTH) + 2;  //Assign the column pixel by scaling up 
																						//left align the character by adding two				
					Y_coord = (Ypos*FONT_HEIGHT) ;		//Assing the row pixel Scale up 
					
					//Draw image
					lcd_draw_image( X_coord, FONT_WIDTH, Y_coord, FONT_HEIGHT,
					sitkaSmall_12ptBitmaps + disp, fg_color, bg_color);
				}
				
		//CASE 2: if column is invalid but the following row is valid		
				else if(Xpos < 0 && Ypos - 1 >= 0 && disp!=0){
					Xpos = Xpos_Orig;									//Assign column to starting column
					Ypos --;													//Move to lower row
					
					X_coord = (Xpos*FONT_WIDTH) + 2;  //Assign the column pixel by scaling up 
																						//left align the character by adding two				
					Y_coord = (Ypos*FONT_HEIGHT) ;		//Assing the row pixel Scale up 
					
					//Draw image
					lcd_draw_image( X_coord, FONT_WIDTH, Y_coord, FONT_HEIGHT,
					sitkaSmall_12ptBitmaps + disp, fg_color, bg_color);
				}
				
		//CASE 3: if both column and following row are invalid 
				else if(Xpos < 0 && Ypos - 1 < 0 && disp!=0){
					Xpos = Xpos_Orig;									//Assign column to starting column
					Ypos = 19;												//Assign to highest row
					
					X_coord = (Xpos*FONT_WIDTH) + 2;  //Assign the column pixel by scaling up 
																						//left align the character by adding two				
					Y_coord = (Ypos*FONT_HEIGHT) ;		//Assing the row pixel Scale up 
					
					//Draw image
					lcd_draw_image( X_coord, FONT_WIDTH, Y_coord, FONT_HEIGHT,
					sitkaSmall_12ptBitmaps + disp, fg_color, bg_color);
				}
					
					
			Xpos --;															//move to next column (from left to right)
				
				
		}	
	}

bool start_pressed(int8_t X, int8_t Y, int8_t num_letter){
	int x; 
	int y;
	int x_start, x_end, y_start, y_end; 
	
	int8_t Xpos;					//holds the comlumn that should be printed into next
	int8_t Ypos;					//holds the row that should be printed into next
	
	int16_t X_coord;			//holds the starting pixel of column based on actual LCD layout
	int16_t Y_coord;			//holds the starting pixel of row based on actual LCD layout
	
	Xpos = 13 - X;				//assigns column from right to left 
	Ypos = 19 - Y;				//assigns column from right to left 
	
	X_coord = (Xpos*FONT_WIDTH) + 2;  //Assign the column pixel by scaling up 
																						//left align the character by adding two				
	Y_coord = (Ypos*FONT_HEIGHT) ;		//Assing the row pixel Scale up 
	
	x_start = X_coord - 2; 
	x_end = (X_coord*num_letter)*FONT_WIDTH + 2; 
	
	y_start = Y_coord - 2; 
	y_end = (Y_coord*FONT_HEIGHT + 2); 
	
	
	
	x = ft6x06_read_x();
	y =  ft6x06_read_y();
	
	if( (x<x_end || x> x_start) && (y<y_end || y> y_start)) return true; 
	
	return false;
}

