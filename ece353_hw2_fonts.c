	int x_start, x_end, y_start, y_end; 
	
	int8_t Xpos;					//holds the comlumn that should be printed into next
	int8_t Ypos;					//holds the row that should be printed into next
	
	int16_t X_coord;			//holds the starting pixel of column based on actual LCD layout
	int16_t Y_coord;			//holds the starting pixel of row based on actual LCD layout
	
	Xpos = 13 - X;				//assigns column from right to left 
	Ypos = 19 - Y;				//assigns column from right to left 
	
	X_coord = (Xpos*FONT_WIDTH) + 2;  //Assign the column pixel by scaling up 
																						//left align the character by adding two				
	Y_coord = (Ypos*FONT_HEIGHT) ;		//Assing the row pixel Scale up 
	
	x_start = X_coord - 2; 
	x_end = (X_coord*num_letter)*FONT_WIDTH + 2; 
	
	y_start = Y_coord - 2; 
	y_end = (Y_coord*FONT_HEIGHT + 2); 
	
	
	x = ft6x06_read_x();
	y =  ft6x06_read_y();
	
	if( (x<x_end || x> x_start) && (y<y_end || y> y_start)) return true; 
	
	
	return false;
