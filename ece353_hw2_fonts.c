//Function: check_X 
//Summary: LCD is 240 pixels wide. The character width must not be beyond 239
//X*17 + 17 is the highest value in character width, and if that value is greater
//than 239, the index is invalid. checks if the width is valid, returns true if  
//X+17 isvalid, otherwise returns false
bool check_X(int16_t *X, int16_t *Y, int16_t *Xpos_Orig, uint32_t disp){
	if (*X >= 0 )
		return true;
		if(disp != 0)
		*X = *Xpos_Orig; 
		*Y = *Y - 1;	
		return false;
}

//Function: check_Y 
//Summary: LCD is 320 pixels tall. The line number can not exceed 19, as index 
//19 corresponds to line number 20, and line 20 fill the very last pixel height
//checks if the high is valid, returns true if  Y < 20 valid, otherwise returns false
bool check_Y(int16_t *Y){
	
	if(*Y >= 0)	return true; 
	
	*Y = (int8_t)319;
	return false;
	
}

