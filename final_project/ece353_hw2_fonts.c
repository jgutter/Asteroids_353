/**********************************************************
* Function Name: lcd_print_stringXY
* Author: Justin Gutter
**********************************************************
* Summary: prints a string to the LCD screen at a specified
* XY location in specified foreground and background colors
* X will specify the number of characters across with X=0
* being the left most character position, and X=13 being
* the right most.  Y will specify the number of characters
* down the screen with Y=0 being the top of the screen and
* Y=19 being the bottom row of characters.  NOTE this is
* opposite of how columns and rows are specified for the
* IL9341.
* Returns:
*  Nothing
**********************************************************/
void lcd_print_stringXY(
    char *msg, 
    int8_t X,
		int8_t Y,
    uint16_t fg_color, 
    uint16_t bg_color
)
{
	int startX = 13 - X;	//index of starting column
	int startY = 19 - Y;	//index of starting row
	int scaledX;					//variable for scaling X index for drawing to LCD screen
	int scaledY;					//variable for scaling Y index for drawing to LCD screen
	int i;								//for loop variable
	for (i = 0; i < strlen(msg); i++) { 		// for loop for length of string
		int c = (int) msg[i]; 								// decimal representation of ascii character
		scaledX = startX * 17;								// multiply by 17 to scale column index for LCD screen
		scaledY = startY * 16;								// multiply by 16 to scale row index for LCD screen
		if ((startX == (13-X)) && (c == 0x20)) 		// if character to print is a space on the start of a new line, skip printing it
			continue;
		c = (c-32)*48; 												//convert decimal ascii value for accessing look up table
		lcd_draw_image(scaledX, FONT_WIDTH, scaledY, FONT_HEIGHT, &sitkaSmall_12ptBitmaps[c], fg_color, bg_color);	//draw character to screen
		startX--;															//decrement startx to next column
		if (startX < 0) {	
			startX = 13 - X;										//if startX is outside of 14 columns, reset X to column X
			startY--;														//and move to new line
		}
		if (startY < 0) {											// if string goes beyond bottom of screen
			startY = 19;												// reset to line 0.
		}
	}
}
